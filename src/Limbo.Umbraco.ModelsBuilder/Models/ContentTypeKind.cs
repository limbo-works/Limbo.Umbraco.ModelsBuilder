namespace Limbo.Umbraco.ModelsBuilder.Models;

/// <summary>
/// Enum class representing the kind of a model.
/// </summary>
public enum ContentTypeKind {

    /// <summary>
    /// Indicates that the model is an element type.
    /// </summary>
    Element,

    /// <summary>
    /// Indicates that the model is a content type.
    /// </summary>
    Content,

    /// <summary>
    /// Indicates that the model is a media type.
    /// </summary>
    Media,

    /// <summary>
    /// Indicates that the model is a member type.
    /// </summary>
    Member

}